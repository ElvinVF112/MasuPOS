type I18nContextType = {
  language: Language
  setLanguage: (language: Language) => void
  t: (key: string) => string
}

const I18nContext = createContext<I18nContextType | null>(null)

export function I18nProvider({ children }: { children: ReactNode }) {
  const pathname = usePathname()
  const [languagePreference, setLanguagePreference] = useState<Language>("es")
  const language: Language = pathname === "/login" ? languagePreference : "es"

  useEffect(() => {
    const stored = window.localStorage.getItem("masu-language")
    if (stored === "es" || stored === "en") {
      setLanguagePreference(stored)
    }
  }, [])

  useEffect(() => {
    window.localStorage.setItem("masu-language", language)
    document.documentElement.lang = language
  }, [language])

  const value = useMemo<I18nContextType>(
    () => ({
      language,
      setLanguage: setLanguagePreference,
      t: (key: string) => translations[key]?.[language] ?? key,
    }),
    [language],
  )

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>
}

export function useI18n() {
  const context = useContext(I18nContext)
  if (!context) {
    throw new Error("useI18n must be used within I18nProvider")
  }
  return context
}
